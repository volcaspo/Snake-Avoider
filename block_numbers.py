styles = [negative_space_nums, eighter_nums, crazy_nums, diagonal_3d]

def numbers_splitter(style):
    numbers_split = {}
    for number in style[2]:
        numbers_split[style[2].index(number)] = style[2][style[2].index(number)].split("\n")
    style.append(numbers_split)

for style in styles:
    style = numbers_splitter(style)

def build_number_lines(number, style):
    """
    Creates a list of lines needed to print a stylized number.
    Style must be a list of lists of lines in each digit.
    """
    num_string = str(number)
    num_lines = ["" for i in range(0, style[0])]
    for digit in num_string:
        for i in range(0, style[0]):
            num_lines[i] += style[3][int(digit)][i]
    return num_lines


